namespace Modding
{
	public enum LogLevel
	{
		//
		// Summary:
		//     Finest Level of Logging - Developers Only
		Fine = 0,
		//
		// Summary:
		//     Debug Level of Logging - Mostly Developers Only
		Debug = 1,
		//
		// Summary:
		//     Normal Logging Level
		Info = 2,
		//
		// Summary:
		//     Only Show Warnings and Above
		Warn = 3,
		//
		// Summary:
		//     Only Show Full Errors
		Error = 4,
		//
		// Summary:
		//     No Logging at all
		Off = 5
	}
}